import my_config
import reward_shaping
from osim.env import *

def generate(env, agent):
    env_m = RunEnv(visualize = True)
    for episode in range(5):
        observation = env.reset(difficulty=0)
        observation_m = env_m.reset(difficulty=0)
        # mirror 对称的
        for step in range(1000):
            action, action_m = get_action(episode, step)
            observation1, reward, done, info = env.step(action)
            shaped_reward = reward_shaping.shape(observation, observation1, reward)
            agent.perceive(observation, action, shaped_reward, observation1, done)
            observation = observation1
            # if done:
            #     my_config.logger.debug("....episode %s, step %s done...." % (episode, step))
            #     my_config.logger.info("replay buffer: %s" % (agent.replay_buffer.count()))
            #     break
            observation1_m, reward_m, done_m, info_m = env_m.step(action_m)
            shaped_reward_m = reward_shaping.shape(observation_m, observation1_m, reward_m)
            agent.perceive(observation_m, action_m, shaped_reward_m, observation1_m, done_m)
            observation_m = observation1_m
            if done_m:
                my_config.logger.debug("....episode %s, step %s done...." % (episode, step))
                my_config.logger.info("replay buffer: %s" % (agent.replay_buffer.count()))
                break

